public class Categories {

    private int id;
    private String engCat;
    private String urduCat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEngCat() {
        return engCat;
    }

    public void setEngCat(String engCat) {
        this.engCat = engCat;
    }

    public String getUrduCat() {
        return urduCat;
    }

    public void setUrduCat(String urduCat) {
        this.urduCat = urduCat;
    }
}
